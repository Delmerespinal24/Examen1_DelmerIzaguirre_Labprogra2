    private void b_aceptarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_b_aceptarActionPerformed
        // TODO add your handling code here:

        String cadena2, cadena3, cadena4, cadena5, cadena6;
        cadena2 = T_nombre.getText();
        cadena3 = Profesion.getText();
        cadena4 = Edad.getText();
        cadena5 = Nick.getText();
        cadena6 = password.getText().toString();

        if (T_nombre.getText().equals("") || Profesion.getText().equals("") || Edad.getText().equals("") || password.getText().toString().equals("")) {
            JOptionPane.showMessageDialog(this, "Debe llenar los campos", "Aviso", JOptionPane.INFORMATION_MESSAGE);
            T_nombre.requestFocus();
        } else {
            lista.add(new Persona(cadena2, cadena3, cadena4, cadena5, cadena6));
            sistema.add(new Github(new Persona(cadena2, cadena3, cadena4, cadena5, cadena6)));
            javax.swing.JOptionPane.showMessageDialog(this, "Registro exitoso! \n", "AVISO!", javax.swing.JOptionPane.INFORMATION_MESSAGE);
        }
        
        
        this.T_nombre.setText("");
        this.Profesion.setText("");
        this.Edad.setText("");
        this.Nick.setText("");
        this.password.setText("");
    }//GEN-LAST:event_b_aceptarActionPerformed

    private void ActualizarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_ActualizarActionPerformed
        // TODO add your handling code here:
        Object[][] matriz = new Object[lista.size()][4];
        for (int i = 0; i < lista.size(); i++) {
            matriz[i][0] = lista.get(i).getNombre();
            matriz[i][1] = lista.get(i).getProfesion();
            matriz[i][2] = lista.get(i).getEdad();
            matriz[i][3] = lista.get(i).getNick();

        }
        Tabla.setModel(new javax.swing.table.DefaultTableModel(
            matriz,
            new String[]{
                "Nombre", "Edad", "Profesion", "Username"
            }));
    }//GEN-LAST:event_ActualizarActionPerformed

    private void DeleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_DeleteActionPerformed
        // TODO add your handling code here:
        int Fila = Tabla.getSelectedRow();
        if(Fila >= 0){
            lista.remove(Fila);
        }else{
            JOptionPane.showMessageDialog(this, "Fila no seleccionada");
        }
    }//GEN-LAST:event_DeleteActionPerformed

    private void LoginActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_LoginActionPerformed
        // TODO add your handling code here:
       
        for (Persona p : lista) {
            if(p.getNick().equals(ingresaruser.getText())){
          
                if(p.getPassword().equals(ingresarpass.getText().toString()));
                for (Github gb : sistema) {
                    if(gb.getuser().getNick().equals(p.getNick())){
                        gb.setVisible(true);
                    }
                }
                
                this.setVisible(false);
            }
            
        }
        
        
    }//GEN-LAST:event_LoginActionPerformed

