    private void T_nombreKeyTyped(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_T_nombreKeyTyped
        // TODO add your handling code here:
        char c = evt.getKeyChar();
        if ((c < 'a' || c > 'z') && (c < 'A' || c > 'Z') && (c > ' ' || c < ' ')) {
            evt.consume();
        }
    }//GEN-LAST:event_T_nombreKeyTyped

